#include<iostream>
using namespace std;
int main(){
    // arithmetic operators
    int a;
    cout << "enter a:";
    cin >> a;
    
    int b;
    cout << "enter b:";
    cin >> b;
    
    cout << "sum: " << a + b << endl;
    cout << "diffrence: " << a - b << endl;
    cout << "product: " << a * b << endl;
    cout <<  "division: " << a / b << endl;
    cout << "modules: " << a % b << endl;

    return 0;
}
